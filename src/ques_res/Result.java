    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      try{
            Class.forName("com.mysql.jdbc.Driver");

            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/examination","root","");
            Statement st=con.createStatement();
            String query="select * from mark_info where uname='"+username+"' and subject='"+sub+"'";
            ResultSet rs=st.executeQuery(query);
            if (rs.next())
            {
                txt1.setText(rs.getString("marks"));
                txt2.setText(rs.getString("grade"));  
            }
            else
            JOptionPane.showMessageDialog(null,"No submissions yet !");
        }catch(Exception e)
        {
            System.out.print(e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

